/// <summary>
/// IGifRect.
/// </summary>
internal interface IGifRect
{
    /// <summary>
    /// Gets the left.
    /// </summary>
    /// <value>
    /// The left.
    /// </value>
    int Left { get; }

    /// <summary>
    /// Gets the top.
    /// </summary>
    /// <value>
    /// The top.
    /// </value>
    int Top { get; }

    /// <summary>
    /// Gets the width.
    /// </summary>
    /// <value>
    /// The width.
    /// </value>
    int Width { get; }

    /// <summary>
    /// Gets the height.
    /// </summary>
    /// <value>
    /// The height.
    /// </value>
    int Height { get; }
}
